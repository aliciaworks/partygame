function main() {
  const csharpDir = path.join(OUT_DIR, "unity/Assets/Scripts/Generated");
  const godotDir = path.join(OUT_DIR, "godot/scripts/generated");
  const unrealDir = path.join(OUT_DIR, "unreal/Source/EdgePulse/Public/Generated");

  // Create directories
  fs.mkdirSync(csharpDir, { recursive: true });
  fs.mkdirSync(godotDir, { recursive: true });
  fs.mkdirSync(unrealDir, { recursive: true });

  // Write C# for Unity
  fs.writeFileSync(path.join(csharpDir, "EdgePulseTypes.cs"), generateCSharp());
  
  // Write GDScript for Godot
  fs.writeFileSync(path.join(godotDir, "edge_pulse_types.gd"), generateGDScript());
  
  // Write C++ for Unreal
  fs.writeFileSync(path.join(unrealDir, "EdgePulseTypes.h"), generateUnrealCPP());

  console.log("Successfully generated types for Unity (C#), Godot (GDScript), and Unreal (C++)!");
}

main();
